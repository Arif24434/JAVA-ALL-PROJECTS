
public class ReverseCounter
{
    public static void main(String args[])
    {
        //loop counter initialisation
        int i=10;

        //print statement
        System.out.println("Output is : ");

        //loop to print 10 to 1.
        while(i>=0)
        {
            System.out.println(i);
            i--;
        }
    }
}

